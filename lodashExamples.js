// lodash documentation - https://lodash.com/docs/4.17.14#shuffle

var _ = require("lodash");

let shuffleResult = _.shuffle([1, 2, 3, 4]);
console.log(`The shuffled Result is: ${shuffleResult}`);

let sumResult = _.sum([1, 3, 6]);
console.log(`The Sum Result is: ${sumResult}`);

let tailResult = _.tail([1, 3, 6]);
console.log(`The Tail Result is ${tailResult}`);
